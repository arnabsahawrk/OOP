# +, -, *, /, //, %, **, <<, >>, &, |, ^, ~
# <, <=, >, >=, ==, !=
# and, or, not

num1 = 45
num2 = 5

print("Addition:", num1 + num2)
print("Subtraction:", num1 - num2)
print("Multiplication:", num1 * num2)
print("Division:", num1 / num2)
print("Floor Division:", num1 // num2)
print("Modulus:", num1 % num2)
print("Exponentiation:", num1 ** num2)
print("Left Shift:", num1 << 2)
print("Right Shift:", num1 >> 2)
print("Bitwise AND:", num1 & num2)
print("Bitwise OR:", num1 | num2)
print("Bitwise XOR:", num1 ^ num2)
print("Bitwise NOT:", ~num1)
print("Less than:", num1 < num2)
print("Less than or equal to:", num1 <= num2)
print("Greater than:", num1 > num2)
print("Greater than or equal to:", num1 >= num2)
print("Equal to:", num1 == num2)
print("Not equal to:", num1 != num2)
print("Logical AND:", num1 > 10 and num2 < 10)
print("Logical OR:", num1 > 10 or num2 < 10)
print("Logical NOT:", not (num1 > 10))
